#!/usr/bin/env python3
"""
leandojo_benchmark_gen.py
─────────────────────────
Reads the LeanDojo benchmark4 random test split and generates a Lean 4
`leanDojo100Decls` list embedded in a §13 block for ErdosHypergraph.lean.

Usage:
    python3 scripts/leandojo_benchmark_gen.py [--n 100] [--max-state 200]

Output:
    Prints the Lean 4 definition to stdout; pipe to inspect or embed.
    Writes data/leandojo_test100.json with the selected theorems.

Design:
    • Deterministic: sorted by full_name, stride-sampled.
    • Filter: keeps theorems whose initial tactic-state ≤ max_state chars
      (simpler goals; reduces false complexity from type-class proliferation).
    • Train/test safety: relies on the official LeanDojo random split, which
      was designed to prevent data leakage (no theorem appears in both sets).
"""

import argparse
import hashlib
import json
import os
import sys
from pathlib import Path

BENCHMARK_DIR = Path("/Volumes/WD-Black/LeanDojo/benchmark4/leandojo_benchmark_4/random")
PROJECT_ROOT  = Path(__file__).parent.parent
OUTPUT_JSON   = PROJECT_ROOT / "data" / "leandojo_test100.json"


def load_split(name: str) -> list[dict]:
    path = BENCHMARK_DIR / f"{name}.json"
    if not path.exists():
        print(f"[ERROR] {path} not found — is the SSD mounted?", file=sys.stderr)
        sys.exit(1)
    with open(path) as f:
        return json.load(f)


def stable_hash(name: str) -> int:
    """Stable 64-bit hash of a string for deterministic sorting tie-breaking."""
    return int(hashlib.sha256(name.encode()).hexdigest()[:16], 16)


def sample_test_theorems(n: int, max_state: int) -> list[dict]:
    """
    Select `n` theorems from test.json deterministically.
    Filter: must have traced_tactics; first state_before ≤ max_state chars.
    Sort: full_name alphabetically, then stride-sample.
    """
    test = load_split("test")
    filtered = [
        t for t in test
        if t.get("traced_tactics")
        and len(t["traced_tactics"][0]["state_before"]) <= max_state
    ]
    # Sort by full_name for reproducibility (stable lexicographic order)
    filtered.sort(key=lambda t: t["full_name"])
    # Stride sample for uniform coverage across the alphabetic range
    step = max(1, len(filtered) // n)
    sample = filtered[::step][:n]
    return sample


def lean_name(full_name: str) -> str:
    """Convert a dotted Lean name to a ``Name`` literal: ``Foo.bar``."""
    # full_name is already the dotted qualified name from LeanDojo
    return full_name


def main():
    parser = argparse.ArgumentParser(description="Generate LeanDojo benchmark target list")
    parser.add_argument("--n",         type=int, default=100, help="Number of targets (default 100)")
    parser.add_argument("--max-state", type=int, default=200, help="Max chars in first tactic state (default 200)")
    parser.add_argument("--lean-only", action="store_true", help="Only print the Lean list, no JSON write")
    args = parser.parse_args()

    sample = sample_test_theorems(args.n, args.max_state)

    if not args.lean_only:
        os.makedirs(OUTPUT_JSON.parent, exist_ok=True)
        with open(OUTPUT_JSON, "w") as f:
            json.dump([{"full_name": t["full_name"],
                        "file_path": t["file_path"],
                        "first_state": t["traced_tactics"][0]["state_before"][:300]}
                       for t in sample], f, indent=2)
        print(f"[OK] Wrote {len(sample)} theorems → {OUTPUT_JSON}", file=sys.stderr)

    print(f"-- Generated by leandojo_benchmark_gen.py  ({len(sample)} targets)")
    print(f"-- Source: LeanDojo benchmark4 random/test.json  (official held-out split)")
    print(f"-- Filter: first tactic-state ≤ {args.max_state} chars  |  deterministic stride sample")
    print()
    print("private def leanDojo100Decls : List Name :=")
    print("  [")
    for i, t in enumerate(sample):
        comma = "," if i < len(sample) - 1 else " "
        comment = t["traced_tactics"][0]["state_before"].split("\n")[-1][:60]
        print(f"    ``{lean_name(t['full_name'])}{comma}  -- ⊢ {comment}")
    print("  ]")
    print()

    # Also print a summary of the first-state distribution
    lengths = [len(t["traced_tactics"][0]["state_before"]) for t in sample]
    print(f"-- State-length p10/p50/p90: "
          f"{sorted(lengths)[len(lengths)//10]}"
          f" / {sorted(lengths)[len(lengths)//2]}"
          f" / {sorted(lengths)[9*len(lengths)//10]}", file=sys.stderr)


if __name__ == "__main__":
    main()
